    //adding doctors in combo box
    public void adddoctors(String speciality)
    {
        
        
        doctors.removeAllItems();
        String query = "select * from doctor WHERE speciality = '" +speciality+ "'";
        try {
            ResultSet rs = Db.getData(query);
            
            while(rs.next())
            {
                String name = rs.getString("name");
                
                doctors.addItem(name);
            }
        } catch (Exception e) {
        }
    }
    

