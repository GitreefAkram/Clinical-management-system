    //registering a patient
    public void register() 
    {
        
        String name = txtname.getText();        
        String contact = txtcontact.getText();
        String address = txtaddress.getText();
        String disease = null;
        String prescribe = null;
        String doctor = (String)doctors.getSelectedItem();
        String gender = gender();
        
        if (name.trim().isEmpty() || contact.trim().isEmpty() || address.trim().isEmpty() || gender == null) {
            JOptionPane.showMessageDialog(addpatient.this, "Fields can't be empty");
        } 
        else {
            try {
                String query = "insert into patient(name, contact, address, disease, prescribe , doctor , Gender) VALUES('"+name+"','"+contact+"','"+address+"','"+disease+"', '" +prescribe+ "' , '"+doctor+"' , '"+gender+"')";
                Db.setData(query);
            } catch (Exception e) {
                System.out.println("" + e.getMessage());
            }
        }

    }
    //it returns the gender selected
    public String gender()
    { 
        String gender = null;
        if (male.isSelected() == true) 
            {
                gender = "male";
            }
        else
            if (female.isSelected() == true) 
            {
                gender = " female";
            }
        else
                if (other.isSelected() == true) 
            {
                gender = "other";
            }
        return gender;
    }
