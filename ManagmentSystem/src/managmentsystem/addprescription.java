    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            int uid = Integer.parseInt(txtid.getText());
            String query = "select * from patient where id = '" + uid + "'";
            ResultSet rs = Db.getData(query);
            if (rs.next()) {
                try {
                    patientname = rs.getString("name");
                    query = "UPDATE patient SET disease='" + txtdisease.getText() + "',prescribe='" + txtprescription.getText() + "' WHERE id='" + uid + "'";                    
                    Db.setData(query);
                    generateReciept();
                    
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Id does not exists..");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

