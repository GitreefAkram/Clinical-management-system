    private void addMedicine(String medicine , String mg)
    {
        
        if (medicine.length() < 11) 
        {
            for (int i = medicine.length(); i <= 11; i++) 
            {
                medicine = medicine + " ";
            }
        }
        
        txtprescription.setText(txtprescription.getText() + medicine + "          " + mg + "\n");
    }
    //genrate reciept
    private void generateReciept()
    {
        String ID = txtid.getText().trim();
        String disease = txtdisease.getText().trim();
        String medicine = txtprescription.getText().trim();
        Date obj = new Date();
        reciept.setText("*** Hospital Management System ***\n");
        reciept.setText(reciept.getText() + "Date = " +obj.toString() + "\n");
        reciept.setText(reciept.getText() + "Doctor = " +name + "\n");
        reciept.setText(reciept.getText() + "Patient = " +patientname + "\n");
        reciept.setText(reciept.getText() + "ID = " +ID + "\n");
        reciept.setText(reciept.getText() + "Disease = " +disease + "\n");
        reciept.setText(reciept.getText() + medicine);
        
    }
